#Computing Practical 1 - Task 1

fahrenheit = input("Enter Fahrenheit Degree: ")

#Get Temperature in Fahrenheit

print("{0:8.2f} F".format(fahrenheit))

#Display temperature in fahrenheit up to 2 decimal places

celcsius = (fahrenheit - 32) * 5 / 9

#Calculate temperature in celcsius

print("{0} C".format(celcsius))

